import sys

# Same routing list as jinjaroot.yaml
KERS = ["c","d","cd","q","cq","dq","cdq",
        "o","co","do","cdo","qo","cqo","dqo","cdqo"]
OUTS = ["p","g","h"]

direct_routs = []
for out in OUTS:
    for ker in KERS:
        name = f"mbh2d_direct{ker}{out}_vec"
        direct_routs.append({"name": name, "ker": ker, "out": out})

eval_routs = [{"out": o} for o in OUTS]

