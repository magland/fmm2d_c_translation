                ympolexx[HI(j,ii+1)] += -beta / 2.0 * ympolex[GI(j,ii)] / rscale;
                ympolexy[HI(j,ii-1)] +=  beta / 2.0 * ympolex[GI(j,ii)] * rscale * eye;
                ympolexy[HI(j,ii+1)] += -beta / 2.0 * ympolex[GI(j,ii)] / rscale * eye;
                ympoleyy[HI(j,ii-1)] +=  beta / 2.0 * ympoley[GI(j,ii)] * rscale * eye;
                ympoleyy[HI(j,ii+1)] += -beta / 2.0 * ympoley[GI(j,ii)] / rscale * eye;

                ympolexx[HI(j,ii-1)]   += -beta / 2.0 * mbhmpolex[GI(j,ii)] * rscale;
                mbhmpolexx[HI(j,ii+1)] += -beta / 2.0 * mbhmpolex[GI(j,ii)] / rscale;
                ympolexy[HI(j,ii-1)]   +=  beta / 2.0 * mbhmpolex[GI(j,ii)] * rscale * eye;
                mbhmpolexy[HI(j,ii+1)] += -beta / 2.0 * mbhmpolex[GI(j,ii)] / rscale * eye;
                ympoleyy[HI(j,ii-1)]   +=  beta / 2.0 * mbhmpoley[GI(j,ii)] * rscale * eye;
                mbhmpoleyy[HI(j,ii+1)] += -beta / 2.0 * mbhmpoley[GI(j,ii)] / rscale * eye;
