def params_for(ker, out):
    """Return parameter list for a direct routine."""
    args = ["const fint *nd_p", "const double *beta_p",
            "const double *source", "const fint *ns_p"]
    if "c" in ker:
        args.append("const double *charge")
    if "d" in ker:
        args += ["const double *dipstr", "const double *dipvec"]
    if "q" in ker:
        args += ["const double *quadstr", "const double *quadvec"]
    if "o" in ker:
        args += ["const double *octstr", "const double *octvec"]
    args += ["const double *targ", "const fint *nt_p"]
    if out in ("p","g","h"):
        args.append("double *pot")
    if out in ("g","h"):
        args.append("double *grad")
    if out == "h":
        args.append("double *hess")
    args.append("const double *thresh_p")
    return args

def emit_direct_routine(rout):
    name = rout["name"]
    ker = rout["ker"]
    out = rout["out"]

    args = params_for(ker, out)
    sig = f"void FNAME({name})(\n    " + ",\n    ".join(args) + ")"

    # Which derivative levels does modbhgreen_all need?
    need_grad = True   # always needed for dipole contrib; charge only needs potloc
    need_hess = True   # needed for dipole grad, charge hess, quad pot
    need_der3 = ("d" in ker and out in ("h",)) or ("q" in ker and out in ("g","h")) or ("o" in ker)
    need_der4 = ("q" in ker and out == "h") or ("o" in ker and out in ("g","h"))
    need_der5 = ("o" in ker and out == "h")

