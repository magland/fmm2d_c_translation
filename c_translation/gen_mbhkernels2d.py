                grad[FA3(j,2,i,nd_v,2)] += creal(mptemp1[0]) * creal(mbhmpoley[GI(j,0)]);
                grad[FA3(j,2,i,nd_v,2)] += creal(mptemp2[0]) * creal(ympoley[GI(j,0)]);
